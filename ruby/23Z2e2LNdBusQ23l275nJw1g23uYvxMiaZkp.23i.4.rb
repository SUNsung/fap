      def compression_option
    case self.attributes[:pacman_compression]
      when nil, 'xz'
        return '--xz'
      when 'none'
        return ''
      when 'gz'
        return '-z'
      when 'bzip2'
        return '-j'
      else
        return '--xz'
      end
  end
    
